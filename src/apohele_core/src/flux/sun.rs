//! Reference Solar Spectrum
//!
//! This is the 2000 ASTM Standard Extraterrestrial Spectrum Reference E-490-00:
//! <https://www.nrel.gov/grid/solar-resource/spectra-astm-e490.html>
//!

use std::f64::consts::PI;

use crate::{
    constants::{AU_KM, C_M_PER_S, SUN_DIAMETER, SUN_TEMP},
    prelude::black_body_flux,
};

/// Return the Solar flux at the provide wavelength using the 2000 ASTM Reference Spectrum.
///
/// This does linear interpolation between the two closest data points.
///
/// Data is available from 119.5 nm to 1mm.
///
/// If query is outside of the range of data, then [`None`] is returned.
///
/// Returned values are units Janskys / steradian.
///
/// # Arguments
///
/// * `dist` - Distance from the Sun in au.
/// * `wavelength` - wavelength in nm.
pub fn solar_flux(dist: f64, wavelength: f64) -> Option<f64> {
    let wavelength = wavelength / 1000.0;
    let start_idx = match E490_DATA.binary_search_by(|probe| probe[0].total_cmp(&wavelength)) {
        Ok(c) => c,
        Err(c) => {
            if c >= E490_DATA.len() - 1 || c == 0 {
                return None;
            };
            c - 1
        }
    };

    let low = E490_DATA[start_idx];
    let high = E490_DATA[start_idx + 1];

    let w_frac = (wavelength - low[0]) / (high[0] - low[0]);
    let val = w_frac * (high[1] - low[1]) + low[1];

    // convert from Watts / m^2 / micron to Jy / Steradian
    Some(val * 1e20 * wavelength.powi(2) / C_M_PER_S / dist.powi(2))
}

/// Calculate the solar flux at the object assuming the sun is a black body disk
/// emitting at the specified wavelength.
///
/// This is an approximation which breaks down when the object gets within a few solar
/// radii.
///
/// This returns the flux at the object in Jy / steradian per unit frequency.
///
/// # Arguments
///
/// * `sun2obj` - Vector from the sun to the object in AU.
/// * `wavelength` - Central wavelength of light in nm.
///
#[inline(always)]
pub fn solar_flux_black_body(dist: f64, wavelength: f64) -> f64 {
    const SUN_R_AU: f64 = SUN_DIAMETER / AU_KM / 2.0;

    // blackbody radiation is Janskys / steradian
    let solar_flux = black_body_flux(SUN_TEMP, wavelength);

    // solar flux at the radius of where the object is, this treats the sun as a flat
    // disk facing the object. This is an approximation which breaks down when the object
    // gets within a few solar radii.
    // Jy / steradian per unit freq
    PI * solar_flux * (SUN_R_AU / dist).powi(2)
}

